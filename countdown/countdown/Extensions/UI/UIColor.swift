extension UIColor {
  
  open class var lightBlue: UIColor {
    return UIColor(red: 11/255, green: 106/255, blue: 255/255, alpha: 1)
  }
  
  open class var keyboardGrey: UIColor {
    return UIColor(red: 210/255, green: 213/255, blue: 219/255, alpha: 1)
  }
}

